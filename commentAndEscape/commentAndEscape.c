// Comments are made with two back slashes
/*
Escape comments are a combo or backslash and a letter or number to do something
\n is new line, \t is tab. \" to literally print a quote
Look up more online if you need them
*/

#include <stdio.h>

int main() {
    printf("Rupert\nIs\nA\nDog\n");
    printf("\tA good boy even..\n");
    printf("1\t2\t3\n4\t5\t6\n7\t8\t9\n");
    printf("\"Sup Dude\" - Someone Important\n");

    return 0;
}